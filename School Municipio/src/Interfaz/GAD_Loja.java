    public void readPDF() {
        // build a controller
        SwingController controller = new SwingController();
        // Build a SwingViewFactory configured with the controller
        SwingViewBuilder factory = new SwingViewBuilder(controller);
        //with a complete, active Viewer UI.
        JPanel viewerComponentPanel = factory.buildViewerPanel();
        // add copy keyboard command
        ComponentKeyBinding.install(controller, viewerComponentPanel);

        // add interactive mouse link annotation support via callback
        controller.getDocumentViewController().setAnnotationCallback(
                new org.icepdf.ri.common.MyAnnotationCallback(
                        controller.getDocumentViewController()));

        // Create a JFrame to display the panel in
        JFrame window = new JFrame("Escuela Municipal Ecol√≥gica");
        window.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        window.setLocation(80, 200);
        window.getContentPane().add(factory.buildCompleteMenuBar(), BorderLayout.NORTH);
        window.getContentPane().add(viewerComponentPanel, BorderLayout.CENTER);
        window.getContentPane().add(basurero, BorderLayout.AFTER_LINE_ENDS);

        window.pack();
        window.setVisible(true);

        // Open a PDF document to view
        // String sr = controller.getDocument().getDocumentOrigin();
        controller.openDocument(filePath);

    }

